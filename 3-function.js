function foo(num){
	if(num === 1){
	console.log("周一")
	}else if(num == 2){
	console.log("周二")
	}else if(num == 3){
	console.log("周三")
	}else if(num == 4){
	console.log("周四")
	}else if(num == 5){
	console.log("周五")
	}else if(num == 6){
	console.log("周六")
	}else if(num == 7){
	console.log("周日")
	}else{
	console.log("错误")
	}
}


foo(3);
